// Sprint 07A — Golden Benchmark Error Taxonomy
// Machine-readable enum + severity table. No dependencies on canonical vNext types.

export const ERROR_CODES = {
  // Classification
  CLASSIFICATION_ERROR: "CLASSIFICATION_ERROR",

  // Entity
  ENTITY_MISS: "ENTITY_MISS",
  ENTITY_MISATTRIBUTION: "ENTITY_MISATTRIBUTION",

  // Value / fact
  VALUE_MISS: "VALUE_MISS",
  VALUE_WRONG: "VALUE_WRONG",
  FALSE_NOT_FOUND: "FALSE_NOT_FOUND",
  FALSE_FOUND: "FALSE_FOUND",
  HALLUCINATION: "HALLUCINATION",

  // Evidence (distinct from hallucination)
  EVIDENCE_MISSING: "EVIDENCE_MISSING",
  EVIDENCE_UNSUPPORTED: "EVIDENCE_UNSUPPORTED",
  PAGE_REFERENCE_WRONG: "PAGE_REFERENCE_WRONG",

  // Line items
  LINE_ITEM_MISS: "LINE_ITEM_MISS",
  LINE_ITEM_FABRICATION: "LINE_ITEM_FABRICATION",
  LINE_ITEM_MATCH_AMBIGUOUS: "LINE_ITEM_MATCH_AMBIGUOUS",

  // Products
  PRODUCT_CONFIG_ERROR: "PRODUCT_CONFIG_ERROR",
  PRODUCT_ASSOCIATION_ERROR: "PRODUCT_ASSOCIATION_ERROR",

  // Cross-cutting
  UNCERTAINTY_MISUSE: "UNCERTAINTY_MISUSE",
  CROSS_DOMAIN_INCONSISTENCY: "CROSS_DOMAIN_INCONSISTENCY",
  ANOMALY_NOT_PRESERVED: "ANOMALY_NOT_PRESERVED",
  CROSS_REFERENCE_DANGLING: "CROSS_REFERENCE_DANGLING",
  CROSS_REFERENCE_INCORRECT: "CROSS_REFERENCE_INCORRECT",

  // Infra (excluded from factual accuracy denominators)
  TRANSPORT_FAILURE: "TRANSPORT_FAILURE",
  PARSE_FAILURE: "PARSE_FAILURE",
  CANONICAL_VALIDATION_FAILURE: "CANONICAL_VALIDATION_FAILURE",

  // Meta
  UNSUPPORTED_BY_SYSTEM: "UNSUPPORTED_BY_SYSTEM",
  HUMAN_REVIEW_REQUIRED: "HUMAN_REVIEW_REQUIRED",
} as const;

export type ErrorCode = typeof ERROR_CODES[keyof typeof ERROR_CODES];

export type Severity = "critical" | "major" | "minor";

export const SEVERITY_TABLE: Record<ErrorCode, Severity> = {
  CLASSIFICATION_ERROR: "major",
  ENTITY_MISS: "major",
  ENTITY_MISATTRIBUTION: "critical",
  VALUE_MISS: "major",
  VALUE_WRONG: "critical",
  FALSE_NOT_FOUND: "major",
  FALSE_FOUND: "major",
  HALLUCINATION: "critical",
  EVIDENCE_MISSING: "minor",
  EVIDENCE_UNSUPPORTED: "major",
  PAGE_REFERENCE_WRONG: "minor",
  LINE_ITEM_MISS: "major",
  LINE_ITEM_FABRICATION: "critical",
  LINE_ITEM_MATCH_AMBIGUOUS: "minor",
  PRODUCT_CONFIG_ERROR: "major",
  PRODUCT_ASSOCIATION_ERROR: "major",
  UNCERTAINTY_MISUSE: "minor",
  CROSS_DOMAIN_INCONSISTENCY: "minor",
  ANOMALY_NOT_PRESERVED: "critical",
  CROSS_REFERENCE_DANGLING: "major",
  CROSS_REFERENCE_INCORRECT: "major",
  TRANSPORT_FAILURE: "minor",
  PARSE_FAILURE: "minor",
  CANONICAL_VALIDATION_FAILURE: "minor",
  UNSUPPORTED_BY_SYSTEM: "minor",
  HUMAN_REVIEW_REQUIRED: "minor",
};

export const CRITICAL_CODES: ReadonlySet<ErrorCode> = new Set(
  (Object.keys(SEVERITY_TABLE) as ErrorCode[]).filter(
    (c) => SEVERITY_TABLE[c] === "critical",
  ),
);

export const INFRA_CODES: ReadonlySet<ErrorCode> = new Set<ErrorCode>([
  ERROR_CODES.TRANSPORT_FAILURE,
  ERROR_CODES.PARSE_FAILURE,
  ERROR_CODES.CANONICAL_VALIDATION_FAILURE,
]);
