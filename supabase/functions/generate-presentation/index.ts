import { createClient, SupabaseClient } from "https://esm.sh/@supabase/supabase-js@2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

// Rate limiting helper
async function checkRateLimit(
  supabase: SupabaseClient,
  identifier: string,
  limit: number,
  windowMs: number
): Promise<boolean> {
  const windowStart = new Date(Date.now() - windowMs).toISOString();
  
  const { count } = await supabase
    .from("rate_limits")
    .select("*", { count: "exact", head: true })
    .eq("identifier", identifier)
    .eq("endpoint", "generate-presentation")
    .gte("created_at", windowStart);

  if ((count ?? 0) >= limit) {
    return false;
  }

  await supabase.from("rate_limits").insert({
    identifier,
    endpoint: "generate-presentation",
  } as Record<string, unknown>);

  return true;
}

// Build prompt based on report type
function buildPrompt(reportType: string, data: Record<string, unknown>): string {
  switch (reportType) {
    case "quote-analysis":
      return buildQuoteAnalysisPrompt(data);
    case "comparison-report":
      return buildComparisonReportPrompt(data);
    default:
      throw new Error(`Unsupported report type: ${reportType}`);
  }
}

