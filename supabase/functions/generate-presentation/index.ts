import { createClient, SupabaseClient } from "https://esm.sh/@supabase/supabase-js@2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

// Rate limiting helper
async function checkRateLimit(
  supabase: SupabaseClient,
  identifier: string,
  limit: number,
  windowMs: number
): Promise<boolean> {
  try {
    const windowStart = new Date(Date.now() - windowMs).toISOString();
    
    const { count, error } = await supabase
      .from("rate_limits")
      .select("*", { count: "exact", head: true })
      .eq("identifier", identifier)
      .eq("endpoint", "generate-presentation")
      .gte("created_at", windowStart);

    if (error) {
      console.error("Rate limit check error:", error);
      // Allow request if rate limit check fails
      return true;
    }

    if ((count ?? 0) >= limit) {
      return false;
    }

    await supabase.from("rate_limits").insert({
      identifier,
      endpoint: "generate-presentation",
    } as Record<string, unknown>);

    return true;
  } catch (err) {
    console.error("Rate limit error:", err);
    return true; // Allow request on error
  }
}

// Build prompt based on report type
function buildPrompt(reportType: string, data: Record<string, unknown>): string {
  switch (reportType) {
    case "quote-analysis":
      return buildQuoteAnalysisPrompt(data);
    case "comparison-report":
      return buildComparisonReportPrompt(data);
    default:
      throw new Error(`Unsupported report type: ${reportType}`);
  }
}

function buildQuoteAnalysisPrompt(data: Record<string, unknown>): string {
  const {
    overallScore,
    safetyScore,
    scopeScore,
    priceScore,
    finePrintScore,
    warrantyScore,
    summary,
    warnings,
    missingItems,
    estimatedPrice,
    priceAssessment,
    homeownerName,
    areaName,
  } = data;

  const warningsList = Array.isArray(warnings) ? warnings.join("\n- ") : "None identified";
  const missingList = Array.isArray(missingItems) ? missingItems.join("\n- ") : "None identified";

  return `Create a professional "Homeowner Window Quote Analysis" presentation for ${homeownerName || "the homeowner"}${areaName ? ` regarding their ${areaName} project` : ""}.

## Executive Summary
${summary || "A comprehensive analysis of your window replacement quote."}

## Overall Assessment
- **Overall Score**: ${overallScore || "N/A"}/100
- **Price Assessment**: ${priceAssessment || "Under review"}
- **Estimated Project Cost**: ${estimatedPrice ? `$${Number(estimatedPrice).toLocaleString()}` : "Not specified"}

## Detailed Scores Breakdown
| Category | Score | Status |
|----------|-------|--------|
| Safety & Compliance | ${safetyScore || "N/A"}/100 | ${getScoreStatus(safetyScore as number)} |
| Scope Completeness | ${scopeScore || "N/A"}/100 | ${getScoreStatus(scopeScore as number)} |
| Price Fairness | ${priceScore || "N/A"}/100 | ${getScoreStatus(priceScore as number)} |
| Fine Print Review | ${finePrintScore || "N/A"}/100 | ${getScoreStatus(finePrintScore as number)} |
| Warranty Coverage | ${warrantyScore || "N/A"}/100 | ${getScoreStatus(warrantyScore as number)} |

## Key Warnings to Address
${warningsList ? `- ${warningsList}` : "No critical warnings identified."}

## Missing Items to Request
${missingList ? `- ${missingList}` : "Quote appears complete."}

## Recommended Next Steps
1. Review the warnings above with your contractor
2. Request written clarification on any missing items
3. Compare with at least 2-3 other quotes before making a decision
4. Schedule a follow-up consultation if needed

---
*This analysis was generated by the It's Window Man Quote Scanner tool to help homeowners make informed decisions about their window replacement projects.*`;
}

function buildComparisonReportPrompt(data: Record<string, unknown>): string {
  const { tiers, windowCount, homeownerName, viewMode } = data;

  const tierData = tiers as Array<{
    name: string;
    pricePerWindow: number;
    trueCost10Year: number;
    features: Record<string, unknown>;
  }>;

  const tierRows = tierData
    ?.map((tier) => {
      const upfrontCost = (tier.pricePerWindow || 0) * (windowCount as number || 10);
      return `| ${tier.name} | $${upfrontCost.toLocaleString()} | $${(tier.trueCost10Year || 0).toLocaleString()} |`;
    })
    .join("\n") || "";

  const savings = tierData && tierData.length >= 2 
    ? (tierData[0].trueCost10Year || 0) - (tierData[tierData.length - 1].trueCost10Year || 0)
    : 0;

  return `Create a professional "Window Quality Comparison Guide" presentation for ${homeownerName || "the homeowner"}.

## Project Overview
- **Number of Windows**: ${windowCount || 10}
- **Analysis Mode**: ${viewMode === "longterm" ? "10-Year True Cost" : "Upfront Price"}

## Side-by-Side Comparison

| Window Tier | Upfront Cost | 10-Year True Cost |
|-------------|--------------|-------------------|
${tierRows}

## Potential Savings
By choosing premium windows over budget options, you could save approximately **$${Math.abs(savings).toLocaleString()}** over 10 years through:
- Reduced energy bills (better insulation)
- Lower maintenance costs
- Fewer replacements needed
- Better warranty coverage

## Key Differentiators

### Budget Windows
- Lower upfront cost
- Basic energy efficiency
- Standard warranty (typically 5-10 years)
- May require earlier replacement

### Mid-Range Windows
- Balanced cost-to-value ratio
- Good energy efficiency
- Extended warranty options
- Suitable for most homes

### Premium Windows
- Highest upfront investment
- Superior energy efficiency
- Lifetime warranties available
- Best long-term value

## Recommendation
${savings > 0 
  ? `Based on the 10-year analysis, investing in higher-quality windows could save you $${Math.abs(savings).toLocaleString()} in the long run.`
  : "Consider your budget, home value, and how long you plan to stay in your home when making your decision."}

---
*This comparison was generated by the It's Window Man Comparison Tool to help homeowners understand the true cost of window replacement.*`;
}

